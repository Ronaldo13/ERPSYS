package com.hqyj.model.xx;

public class WareHouseModel {
    private Integer productionWarehouseId;

    private String productionName;

    private Integer productionNumber;

    private String productionType;

    public Integer getProductionWarehouseId() {
        return productionWarehouseId;
    }

    public void setProductionWarehouseId(Integer productionWarehouseId) {
        this.productionWarehouseId = productionWarehouseId;
    }

    public String getProductionName() {
        return productionName;
    }

    public void setProductionName(String productionName) {
        this.productionName = productionName;
    }

    public Integer getProductionNumber() {
        return productionNumber;
    }

    public void setProductionNumber(Integer productionNumber) {
        this.productionNumber = productionNumber;
    }

    public String getProductionType() {
        return productionType;
    }

    public void setProductionType(String productionType) {
        this.productionType = productionType;
    }

	@Override
	public String toString() {
		return "WareHouseModel [productionWarehouseId=" + productionWarehouseId + ", productionName=" + productionName
				+ ", productionNumber=" + productionNumber + ", productionType=" + productionType + "]";
	}
    
}