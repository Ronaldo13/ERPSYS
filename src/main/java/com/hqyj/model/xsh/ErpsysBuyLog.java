package com.hqyj.model.xsh;

import java.util.Date;

public class ErpsysBuyLog {
    private Integer buyLogId;

    private String seller;

    private Date buyTime;

    private Integer buyNumber;

    @Override
	public String toString() {
		return "ErpsysBuyLog [buyLogId=" + buyLogId + ", seller=" + seller + ", buyTime=" + buyTime + ", buyNumber="
				+ buyNumber + ", inWarehouseLogId=" + inWarehouseLogId + ", orderId=" + orderId + "]";
	}

	private Integer inWarehouseLogId;

    private Integer orderId;

    public Integer getBuyLogId() {
        return buyLogId;
    }

    public void setBuyLogId(Integer buyLogId) {
        this.buyLogId = buyLogId;
    }

    public String getSeller() {
        return seller;
    }

    public void setSeller(String seller) {
        this.seller = seller;
    }

    public Date getBuyTime() {
        return buyTime;
    }

    public void setBuyTime(Date buyTime) {
        this.buyTime = buyTime;
    }

    public Integer getBuyNumber() {
        return buyNumber;
    }

    public void setBuyNumber(Integer buyNumber) {
        this.buyNumber = buyNumber;
    }

    public Integer getInWarehouseLogId() {
        return inWarehouseLogId;
    }

    public void setInWarehouseLogId(Integer inWarehouseLogId) {
        this.inWarehouseLogId = inWarehouseLogId;
    }

    public Integer getOrderId() {
        return orderId;
    }

    public void setOrderId(Integer orderId) {
        this.orderId = orderId;
    }
}