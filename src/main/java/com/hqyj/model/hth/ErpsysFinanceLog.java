package com.hqyj.model.hth;

import java.util.Date;

public class ErpsysFinanceLog {
    private Integer financeLogId;

    private String operation;

    private String accountId;

    private Date operationTime;
    
    private String operationTimeString;

    public Integer getFinanceLogId() {
        return financeLogId;
    }

    public void setFinanceLogId(Integer financeLogId) {
        this.financeLogId = financeLogId;
    }

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getAccountId() {
        return accountId;
    }

    public void setAccountId(String accountId) {
        this.accountId = accountId;
    }

    public Date getOperationTime() {
        return operationTime;
    }

    public void setOperationTime(Date operationTime) {
        this.operationTime = operationTime;
    }

	public String getOperationTimeString() {
		return operationTimeString;
	}

	public void setOperationTimeString(String operationTimeString) {
		this.operationTimeString = operationTimeString;
	}
    
    
}